func (l *Logger) formatHeader(level int, buf *[]byte, t time.Time, file string, line int) {

	*buf = append(*buf, l.prefix...)
	if l.flag&LUTC != 0 {
		t = t.UTC()
	}

	color := "ðŸ’œ"
	if !l.Todb {
		switch level {
		case 0:
			// Fatl
			color = "â¤ï¸"
		case 1:
			// error
			color = "â¤ï¸"
		case 2:
			// warn
			color = "ðŸ’›"
		case 3:
			// info
			color = "ðŸ’™"
		case 4:
			//debug
			color = "ðŸ’š"
		case 5:
			// trace
			color = "ðŸ’œ"
		default:
			// trace
			color = "ðŸ’œ"
		}
	}

	// å¢žåŠ æ—¶é—´
	if l.flag&(Ldate|Ltime|Lmicroseconds) != 0 {
		if l.flag&Ldate != 0 {
			year, month, day := t.Date()
			itoa(buf, year, 4)
			*buf = append(*buf, '/')
			itoa(buf, int(month), 2)
			*buf = append(*buf, '/')
			itoa(buf, day, 2)
			*buf = append(*buf, ' ')
		}
		if l.flag&(Ltime|Lmicroseconds) != 0 {
			hour, min, sec := t.Clock()
			itoa(buf, hour, 2)
			*buf = append(*buf, ':')
			itoa(buf, min, 2)
			*buf = append(*buf, ':')
			itoa(buf, sec, 2)
			if l.flag&Lmicroseconds != 0 {
				*buf = append(*buf, '.')
				itoa(buf, t.Nanosecond()/1e3, 6)
			}
			*buf = append(*buf, ' ')
		}
	}

	// å¢žåŠ æ ‡å¿—
	switch level {
	case 0:
		tmp := "[FATL] "
		*buf = append(*buf, tmp...)
	case 1:
		tmp := "[EROR] "
		*buf = append(*buf, tmp...)
	case 2:
		tmp := "[WARN] "
		*buf = append(*buf, tmp...)
	case 3:
		tmp := "[INFO] "
		*buf = append(*buf, tmp...)
	case 4:
		tmp := "[DEBG] "
		*buf = append(*buf, tmp...)
	case 5:
		tmp := "[TRCE] "
		*buf = append(*buf, tmp...)
	default:
		tmp := "[TRCE] "
		*buf = append(*buf, tmp...)
	}

	if l.flag&(Lshortfile|Llongfile) != 0 {
		if l.flag&Lshortfile != 0 {
			short := file
			for i := len(file) - 1; i > 0; i-- {
				if file[i] == '/' {
					short = file[i+1:]
					break
				}
			}
			file = short
		}
		*buf = append(*buf, file...)
		*buf = append(*buf, ':')
		itoa(buf, line, -1)
		*buf = append(*buf, ": "...)
		*buf = append(*buf, color...)
	}
}

// Output writes the output for a logging event. The string s contains
// the text to print after the prefix specified by the flags of the
// Logger. A newline is appended if the last character of s is not
// already a newline. Calldepth is used to recover the PC and is
// provided for generality, although at the moment on all pre-defined
// paths it will be 2.
func (l *Logger) Output(level int, calldepth int, s string) error {
	now := time.Now() // get this early.
	var file string
	var line int
	l.mu.Lock()
	defer l.mu.Unlock()
	if l.flag&(Lshortfile|Llongfile) != 0 {
		// release lock while getting caller info - it's expensive.
		l.mu.Unlock()
		var ok bool
		_, file, line, ok = runtime.Caller(calldepth)
		if !ok {
			file = "???"
			line = 0
		}
		l.mu.Lock()
	}
	l.buf = l.buf[:0]
	l.formatHeader(level, &l.buf, now, file, line)

	//fmt.Println(s)
	s = string(([]byte(s))[1 : len(s)-2])
	//fmt.Println(s)
	l.buf = append(l.buf, s...)
	if len(s) == 0 || s[len(s)-1] != '\n' {
		l.buf = append(l.buf, '\n')
	}

	//add by llh 2017-02-07
	if len(s) >= 2 && s[len(s)-1] == '\n' && s[len(s)-2] == '\n' {
		l.buf = l.buf[:len(s)-1]
	}

	_, err := l.out.Write(l.buf)

	return err
}

// Printf calls l.Output to print to the logger.
// Arguments are handled in the manner of fmt.Printf.
func (l *Logger) Printf(format string, v ...interface{}) {
	l.Output(4, 2, fmt.Sprintf(format, v...))
}

// Print calls l.Output to print to the logger.
// Arguments are handled in the manner of fmt.Print.
func (l *Logger) Print(v ...interface{}) { l.Output(4, 2, fmt.Sprint(v...)) }

// Println calls l.Output to print to the logger.
// Arguments are handled in the manner of fmt.Println.
func (l *Logger) Println(v ...interface{}) { l.Output(4, 2, fmt.Sprintln(v...)) }

// Fatal is equivalent to l.Print() followed by a call to os.Exit(1).
func (l *Logger) Fatal(v ...interface{}) {
	l.Output(4, 2, fmt.Sprint(v...))
	os.Exit(1)
}

// Fatalf is equivalent to l.Printf() followed by a call to os.Exit(1).
